package com.example.myapplication;

public class NearbyCities {
    private String city1;
    private String city2;

    public NearbyCities(String city1, String city2) {
        this.city1 = city1;
        this.city2 = city2;
    }

    public String getCity1() {
        return city1;
    }

    public String getCity2() {
        return city2;
    }
}